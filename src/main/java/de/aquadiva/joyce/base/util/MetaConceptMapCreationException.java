package de.aquadiva.joyce.base.util;

public class MetaConceptMapCreationException extends JoyceException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8131751470943426265L;

	public MetaConceptMapCreationException() {
	}

	public MetaConceptMapCreationException(String message) {
		super(message);
	}

	public MetaConceptMapCreationException(Throwable cause) {
		super(cause);
	}

	public MetaConceptMapCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public MetaConceptMapCreationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
